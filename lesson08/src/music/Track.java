package music;

import music.Artist;

public class Track {
    public String trackName;
    double numberOfSeconds;
    int publishingYear;
    Artist artist;

    public Track(String trackName, double numberOfSeconds, int publishingYear, Artist artist){
        this.trackName = trackName;
        this.numberOfSeconds = numberOfSeconds;
        this.publishingYear = publishingYear;
        this.artist = artist;
    }








}
